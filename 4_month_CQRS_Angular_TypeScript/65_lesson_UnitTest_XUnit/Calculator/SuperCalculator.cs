namespace Calculator
{
    public class SuperCalculator
    {
        public int Add(int x, int y)
        {
            return x + y;
        }
        public int Devide(int x, int y)
        {
            return x / y;
        }
        public int Substract(int x, int y)
        {
            return x - y;
        }
        public int Multiple(int x, int y)
        {
            return x * y;
        }

    }
}
